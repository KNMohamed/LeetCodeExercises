package com.leetcode.challenges.easy;

public class ListNode {
    // Definition for singly-linked list.
    int val;
    com.leetcode.challenges.easy.ListNode next;
    ListNode() {}
    ListNode(int val) { this.val = val; }
    ListNode(int val, com.leetcode.challenges.easy.ListNode next) { this.val = val; this.next = next; }
}
